TARGET_HORIZONS = [1, 3, 5, 7]  # min — Kapral forecasts to 7 min only; no reference beyond.


def main():
    # tidy raw dump, sorted by x within each (dataset, curve)
    with open("results/kapral_mae_curves.csv", "w", newline="") as fh:
        w = csv.writer(fh)
        w.writerow(["dataset", "curve", "forecast_min", "mae_mmHg"])
        for (ds, curve), pts in DATA.items():
            for x, y in sorted(pts):
                w.writerow([ds, curve, f"{x:.6f}", f"{y:.6f}"])
    print("wrote results/kapral_mae_curves.csv")

    # interpolate mean/lo/hi to the comparison horizons + report the 0..7 min average (sanity)
    def interp(ds, curve, xq):
        pts = sorted(DATA[(ds, curve)])
        xs = np.array([p[0] for p in pts]); ys = np.array([p[1] for p in pts])
        return float(np.interp(xq, xs, ys))

    with open("results/kapral_mae_at_horizons.csv", "w", newline="") as fh:
        w = csv.writer(fh)
        w.writerow(["dataset", "forecast_min", "mae_mean", "mae_lower", "mae_upper"])
        for ds in ("internal", "external"):
            for h in TARGET_HORIZONS:
                w.writerow([ds, h, f"{interp(ds,'mean',h):.3f}",
                            f"{interp(ds,'lower',h):.3f}", f"{interp(ds,'upper',h):.3f}"])
    print("wrote results/kapral_mae_at_horizons.csv\n")

    print(f"{'dataset':9s} {'h(min)':>6s} {'mean':>6s} {'lower':>6s} {'upper':>6s}")
    for ds in ("internal", "external"):
        for h in TARGET_HORIZONS:
            print(f"{ds:9s} {h:6d} {interp(ds,'mean',h):6.2f} "
                  f"{interp(ds,'lower',h):6.2f} {interp(ds,'upper',h):6.2f}")
        # sanity: horizon-average of the mean curve over 0..7 min vs paper (internal 4 / external 7 mmHg)
        pts = sorted(DATA[(ds, "mean")]); xs=np.array([p[0] for p in pts]); ys=np.array([p[1] for p in pts])
        grid = np.linspace(0, 7, 141)
        print(f"  -> 0..7min avg of mean curve = {np.interp(grid,xs,ys).mean():.2f} mmHg "
              f"(paper: {'4' if ds=='internal' else '7'} mmHg)")


if __name__ == "__main__":
    main()
